import pandas as pd
import numpy as np
import joblib
import gradio as gr
import matplotlib.pyplot as plt

from sklearn.model_selection import train_test_split
from sklearn.compose import ColumnTransformer
from sklearn.pipeline import Pipeline
from sklearn.impute import SimpleImputer
from sklearn.preprocessing import OneHotEncoder, StandardScaler, LabelEncoder
from sklearn.metrics import accuracy_score, precision_score, recall_score, f1_score, roc_auc_score, confusion_matrix, ConfusionMatrixDisplay, roc_curve, auc
from sklearn.inspection import permutation_importance
from sklearn.linear_model import LogisticRegression
from sklearn.ensemble import RandomForestClassifier
from sklearn.svm import SVC
from sklearn.neighbors import KNeighborsClassifier

# Load dataset (make sure file is in same folder)
df = pd.read_csv("HeartDiseaseTrain-Test.csv")

target_col = "target"
X = df.drop(columns=[target_col])
y = df[target_col]

label_encoder = LabelEncoder()
y_encoded = label_encoder.fit_transform(y)

numeric_features = X.select_dtypes(include=["int64", "float64"]).columns.tolist()
categorical_features = X.select_dtypes(include=["object"]).columns.tolist()

numeric_transformer = Pipeline([
    ("imputer", SimpleImputer(strategy="median")),
    ("scaler", StandardScaler())
])

categorical_transformer = Pipeline([
    ("imputer", SimpleImputer(strategy="most_frequent")),
    ("onehot", OneHotEncoder(handle_unknown="ignore"))
])

preprocessor = ColumnTransformer([
    ("num", numeric_transformer, numeric_features),
    ("cat", categorical_transformer, categorical_features)
])

X_train, X_test, y_train, y_test = train_test_split(
    X, y_encoded, test_size=0.2, random_state=42, stratify=y_encoded
)

models = {
    "Logistic Regression": LogisticRegression(max_iter=1000, random_state=42),
    "Random Forest": RandomForestClassifier(n_estimators=100, max_depth=5, random_state=42),
    "SVM": SVC(probability=True, random_state=42),
    "KNN": KNeighborsClassifier(n_neighbors=5)
}

trained_models = {}
results = []

for name, clf in models.items():
    pipe = Pipeline([
        ("preprocessor", preprocessor),
        ("model", clf)
    ])
    pipe.fit(X_train, y_train)
    trained_models[name] = pipe

    y_pred = pipe.predict(X_test)
    y_prob = pipe.predict_proba(X_test)[:, 1]

    results.append({
        "Model": name,
        "Accuracy": accuracy_score(y_test, y_pred),
        "Precision": precision_score(y_test, y_pred),
        "Recall": recall_score(y_test, y_pred),
        "F1 Score": f1_score(y_test, y_pred),
        "ROC AUC": roc_auc_score(y_test, y_prob)
    })

results_df = pd.DataFrame(results).sort_values(by="F1 Score", ascending=False)
print(results_df)

# Plot model comparison
plt.figure(figsize=(10, 6))
metrics_to_plot = ["Accuracy", "Precision", "Recall", "F1 Score", "ROC AUC"]
x = np.arange(len(results_df["Model"]))
width = 0.15

for i, metric in enumerate(metrics_to_plot):
    plt.bar(x + i * width, results_df[metric], width=width, label=metric)

plt.xticks(x + width * 2, results_df["Model"], rotation=15)
plt.ylim(0, 1)
plt.title("Model Comparison")
plt.legend()
plt.tight_layout()
plt.savefig("model_comparison.png")
plt.close()

# ROC Curve
plt.figure(figsize=(8, 6))
for name, pipe in trained_models.items():
    y_prob = pipe.predict_proba(X_test)[:, 1]
    fpr, tpr, _ = roc_curve(y_test, y_prob)
    roc_auc = auc(fpr, tpr)
    plt.plot(fpr, tpr, label=f"{name} (AUC = {roc_auc:.3f})")

plt.plot([0, 1], [0, 1], linestyle="--")
plt.xlabel("False Positive Rate")
plt.ylabel("True Positive Rate")
plt.title("ROC Curve Comparison")
plt.legend()
plt.tight_layout()
plt.savefig("roc_curve.png")
plt.close()

best_model_name = results_df.iloc[0]["Model"]
best_model = trained_models[best_model_name]
best_pred = best_model.predict(X_test)

# Confusion Matrix
cm = confusion_matrix(y_test, best_pred)
disp = ConfusionMatrixDisplay(confusion_matrix=cm, display_labels=label_encoder.classes_)
disp.plot()
plt.title(f"Confusion Matrix - {best_model_name}")
plt.tight_layout()
plt.savefig("confusion_matrix.png")
plt.close()

# Feature Importance
perm = permutation_importance(best_model, X_test, y_test, n_repeats=10, random_state=42, scoring="f1")

importance_df = pd.DataFrame({
    "Feature": X_test.columns,
    "Importance": perm.importances_mean
}).sort_values(by="Importance", ascending=False).head(15)

plt.figure(figsize=(10, 6))
plt.barh(importance_df["Feature"][::-1], importance_df["Importance"][::-1])
plt.title(f"Top 15 Feature Importances - {best_model_name}")
plt.tight_layout()
plt.savefig("feature_importance.png")
plt.close()

# Save models
joblib.dump(trained_models, "heart_models.pkl")
joblib.dump(label_encoder, "label_encoder.pkl")
joblib.dump(results_df, "model_results.pkl")

def get_choices(col):
    return sorted(df[col].dropna().astype(str).unique())

def predict_heart(
    selected_model, age, sex, cp, bp, chol, fbs, restecg,
    hr, exang, oldpeak, slope, ca, thal
):
    data = pd.DataFrame([{
        "age": age,
        "sex": sex,
        "chest_pain_type": cp,
        "resting_blood_pressure": bp,
        "cholestoral": chol,
        "fasting_blood_sugar": fbs,
        "rest_ecg": restecg,
        "Max_heart_rate": hr,
        "exercise_induced_angina": exang,
        "oldpeak": oldpeak,
        "slope": slope,
        "vessels_colored_by_flourosopy": ca,
        "thalassemia": thal
    }])

    model = trained_models[selected_model]
    prob = model.predict_proba(data)[0][1]
    pred = 1 if prob >= 0.5 else 0
    label = label_encoder.inverse_transform([pred])[0]
    result = "Disease" if str(label) in ["1", "Disease"] else "No Disease"

    metrics_row = results_df[results_df["Model"] == selected_model].iloc[0]

    color = "#ff4d4d" if result == "Disease" else "#4CAF50"
    emoji = "⚠️" if result == "Disease" else "✅"

    return f"""
    <div style="padding:25px;border-radius:15px;background:#111827;color:white;text-align:center;">
        <h2>{emoji} Prediction Result</h2>
        <h1 style="color:{color};">{result}</h1>
        <p>Selected Model</p>
        <h3 style="color:#60a5fa;">{selected_model}</h3>
        <p>Probability</p>
        <h2 style="color:#facc15;">{round(prob*100,2)}%</h2>
        <hr>
        <h3>Model Performance</h3>
        <p>Accuracy: {metrics_row['Accuracy']:.4f}</p>
        <p>Precision: {metrics_row['Precision']:.4f}</p>
        <p>Recall: {metrics_row['Recall']:.4f}</p>
        <p>F1 Score: {metrics_row['F1 Score']:.4f}</p>
        <p>ROC AUC: {metrics_row['ROC AUC']:.4f}</p>
    </div>
    """

with gr.Blocks() as app:
    gr.Markdown("# ❤️ Heart Disease Prediction App")
    gr.Markdown(f"### Best Model: {best_model_name}")

    with gr.Row():
        with gr.Column():
            model_choice = gr.Dropdown(list(trained_models.keys()), value=best_model_name, label="Choose Model")
            age = gr.Number(value=50, label="Age")
            sex = gr.Dropdown(get_choices("sex"), label="Sex")
            cp = gr.Dropdown(get_choices("chest_pain_type"), label="Chest Pain Type")
            bp = gr.Number(value=120, label="Resting BP")
            chol = gr.Number(value=200, label="Cholesterol")
            fbs = gr.Dropdown(get_choices("fasting_blood_sugar"), label="Fasting Blood Sugar")
            restecg = gr.Dropdown(get_choices("rest_ecg"), label="Rest ECG")

        with gr.Column():
            hr = gr.Number(value=150, label="Max Heart Rate")
            exang = gr.Dropdown(get_choices("exercise_induced_angina"), label="Exercise Induced Angina")
            oldpeak = gr.Number(value=1.0, label="Oldpeak")
            slope = gr.Dropdown(get_choices("slope"), label="Slope")
            ca = gr.Dropdown(get_choices("vessels_colored_by_flourosopy"), label="Vessels Colored")
            thal = gr.Dropdown(get_choices("thalassemia"), label="Thalassemia")

    btn = gr.Button("Predict")
    output = gr.HTML()

    btn.click(
        predict_heart,
        inputs=[model_choice, age, sex, cp, bp, chol, fbs, restecg, hr, exang, oldpeak, slope, ca, thal],
        outputs=output
    )

if __name__ == "__main__":
    app.launch()